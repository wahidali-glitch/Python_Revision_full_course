import math
x = 1 # Integer
y = 2.5 # Float
z = 1 + 2.5 # Result will be a Float
print(x) # Integer
print(y) # Float
print(z) # Float

# x = a + bi where a is the real part and b is the imaginary part pi for I
# complex numbers are used in advanced mathematics and engineering applications
a = 2 + 3j
b = 1 + 2j
c = a + b
print(c) # Complex number
print( 10 +3)
print(10 - 3)
print(10 * 3)   
print(10 / 3) # this will return a float
print(10 // 3) # this will return an integer    
print(10 % 3) # this will return the remainder of the division
print(10 ** 3) # this will return 10 raised to the power of 3
