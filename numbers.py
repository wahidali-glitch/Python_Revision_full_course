print(round(2.9)) # this will round the number to the nearest integer
print(abs(-2.9)) # this will return the absolute value of the number
print(math.ceil(2.9)) # this will return the smallest integer greater than or equal to the number
print(math.floor(2.9)) # this will return the largest integer less than or equal to the number
