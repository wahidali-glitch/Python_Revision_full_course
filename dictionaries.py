# A dictionary in python is like a real-life dictionary 
# it has keys and values
# keys are like words and values are like the meaning of the words
# Why do we use dictionaries
# They allow us to store and retreive data quickly
# e.g:
student = {
    "name": "Ali",
    "roll_number": 123,
    "grade": "A"}
